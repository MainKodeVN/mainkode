-- Creating databases
CREATE DATABASE RAW COMMENT = 'Production landing zone for raw data';
CREATE DATABASE ANALYTICS COMMENT = 'Production data layer for analytics';
CREATE DATABASE RAW_DEV CLONE RAW COMMENT = 'Development landing zone for raw data';
CREATE DATABASE ANALYTICS_DEV CLONE ANALYTICS COMMENT = 'Development data layer for analytics';
CREATE DATABASE RAW_QA CLONE RAW COMMENT = 'QA landing zone for raw data';
CREATE DATABASE ANALYTICS_QA CLONE ANALYTICS COMMENT = 'QA data layer for analytics';

-- Creating warehouses
CREATE WAREHOUSE LOADING WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'Production warehouse for data loading';
CREATE WAREHOUSE TRANSFORMING WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'Production warehouse for data transformation';
CREATE WAREHOUSE REPORTING WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'Production warehouse for BI and reporting';

CREATE WAREHOUSE LOADING_DEV WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'Development warehouse for data loading';
CREATE WAREHOUSE TRANSFORMING_DEV WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'Development warehouse for data transformation';
CREATE WAREHOUSE REPORTING_DEV WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'Development warehouse for BI and reporting';

CREATE WAREHOUSE LOADING_QA WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'QA warehouse for data loading';
CREATE WAREHOUSE TRANSFORMING_QA WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'QA warehouse for data transformation';
CREATE WAREHOUSE REPORTING_QA WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' AUTO_SUSPEND = 300 AUTO_RESUME = TRUE COMMENT = 'QA warehouse for BI and reporting';

-- Creating roles for each environment
CREATE ROLE LOADER COMMENT = 'Role for production data ingestion';
CREATE ROLE TRANSFORMER COMMENT = 'Role for production data transformations';
CREATE ROLE REPORTER COMMENT = 'Role for production BI tools and data consumers';

CREATE ROLE LOADER_DEV COMMENT = 'Role for development data ingestion';
CREATE ROLE TRANSFORMER_DEV COMMENT = 'Role for development data transformations';
CREATE ROLE REPORTER_DEV COMMENT = 'Role for development BI tools and data consumers';

CREATE ROLE LOADER_QA COMMENT = 'Role for QA data ingestion';
CREATE ROLE TRANSFORMER_QA COMMENT = 'Role for QA data transformations';
CREATE ROLE REPORTER_QA COMMENT = 'Role for QA BI tools and data consumers';

-- Granting permissions for production LOADER role
GRANT USAGE, OPERATE ON WAREHOUSE LOADING TO ROLE LOADER;
GRANT ALL ON DATABASE RAW TO ROLE LOADER;
GRANT CREATE TABLE ON ALL SCHEMAS IN DATABASE RAW TO ROLE LOADER;
GRANT CREATE TABLE ON FUTURE SCHEMAS IN DATABASE RAW TO ROLE LOADER;

-- Granting permissions for production TRANSFORMER role
GRANT USAGE, OPERATE ON WAREHOUSE TRANSFORMING TO ROLE TRANSFORMER;
