DROP ROLE IF EXISTS TRANSFORMER;
DROP ROLE IF EXISTS REPORTER;
DROP ROLE IF EXISTS LOADER_DEV;
DROP ROLE IF EXISTS TRANSFORMER_DEV;
DROP ROLE IF EXISTS REPORTER_DEV;
DROP ROLE IF EXISTS LOADER_QA;
DROP ROLE IF EXISTS TRANSFORMER_QA;
DROP ROLE IF EXISTS REPORTER_QA;

